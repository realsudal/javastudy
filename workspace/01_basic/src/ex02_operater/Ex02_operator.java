package ex02_operater;

public class Ex02_operator {

	public static void main(String[] args) {
		
		//관계연산자 <,>,!=,==,<=,>=
		
		int age=20;
		boolean isAdult = age>=20;
		System.out.println(isAdult);
		
		//논리연산자 &&,||,!    <-대부분 관계연산자와 함께 쓰임
		/*
		 * && = * = and
		 * || = + = or
		 * 
		 */
		
		int a=10; 
		System.out.println(!(a==10));
		
		
		
		
		
	}

}
