package ex05_loop;

public class Ex03_for {

	public static void main(String[] args) {
		//for문
		//1. 반복의 횟수나 범위를 알 떄 사용 ex)배열,리스트
		//2. 반복가능 객체 - > 리터럴에서 사용한다.
		
		
		
		//1~10 출력
		for(int i=1;i<=10;i++) {
			System.out.println(i);
		}
		
		//10~1 출력
			for(int i=10;i>=1;i--) {
				System.out.println(i);
			}
		
	}

}
