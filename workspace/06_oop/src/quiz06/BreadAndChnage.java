package quiz06;

public class BreadAndChnage {

	private int bread;
	private int change;
	
	
	
	
	//빵과 잔돈을 관리하는 메소드
	public BreadAndChnage(int bread,int change) {
		this.bread=bread;
		this.change=change;
	}




	
	
	
	
	
	
	
	
	
	
	public int getBread() {
		return bread;
	}




	public void setBread(int bread) {
		this.bread = bread;
	}




	public int getChange() {
		return change;
	}




	public void setChange(int change) {
		this.change = change;
	}
	
	
}
