package quiz01;

public class Calculator {

	
	private int result;
	
	public void addion(int a) {
		result+=a;
		System.out.println(result);
	}
	public void sub(int a) {
		result-=a;
		System.out.println(result);
	}
	public void div(int a) {
		result/=a;
		System.out.println(result);
	}
	public void multi(int a) {
		result*=a;
		System.out.println(result);
	}
	
	
	
}
